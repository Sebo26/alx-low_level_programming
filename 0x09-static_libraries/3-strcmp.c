#include <string.h>

/**
 * _strcmp - The function compares two strings.
 * @s1: first string of the two to be compared.
 * @s2: second string of the two to be compared.
 * Return: n (degree of how similar the strings are.
 */
int _strcmp(char *s1, char *s2)
{
int n;
n = strcmp(s1, s2);

return (n);
}
