#include <stdio.h>
/**
 * add - adds two numbers
 * @m: first number
 * @n: second number
 * Return: sum
 */
int add(int m, int n)
{
	return (m + n);
}

/**
 * sub - subtracts two numbers
 * @m: first number
 * @n: second number
 * Return: difference
 */
int sub(int m, int n)
{
	return (m - n);
}

/**
 * divide - divides two numbers
 * @m: first number
 * @n: second number
 * Return: division
 */
int divide(int m, int n)
{
	return (m / n);
}

/**
 * mod - adds two numbers
 * @m: first number
 * @n: second number
 * Return: remainder
 */
int mod(int m, int n)
{
	return (m % n);
}
/**
 * mul - adds two numbers
 * @m: first number
 * @n: second number
 * Return: product
 */
int mul(int m, int n)
{
	return (m * n);
}
